// Dedicated Authentication Page - Login & Signup

import { useState, useEffect } from 'react';
import { motion } from 'framer-motion';
import { useNavigate, useSearchParams } from 'react-router-dom';
import { Mail, Sparkles, Loader2, ArrowLeft, KeyRound, Home } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { useUserStore } from '@/stores/userStore';
import { supabase } from '@/integrations/supabase/client';
import { toast } from '@/hooks/use-toast';
import { cn } from '@/lib/utils';

// Google Icon
const GoogleIcon = () => (
  <svg viewBox="0 0 24 24" className="w-5 h-5">
    <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
    <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
    <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
    <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
  </svg>
);

export default function Auth() {
  const navigate = useNavigate();
  const [searchParams] = useSearchParams();
  const { isLoggedIn, login } = useUserStore();
  
  const [mode, setMode] = useState<'signup' | 'login' | 'forgot'>(
    searchParams.get('mode') === 'login' ? 'login' : 'signup'
  );
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [name, setName] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const [loadingProvider, setLoadingProvider] = useState<string | null>(null);
  const [resetEmailSent, setResetEmailSent] = useState(false);

  // Redirect if already logged in
  useEffect(() => {
    if (isLoggedIn) {
      const returnTo = searchParams.get('returnTo') || '/';
      navigate(returnTo);
    }
  }, [isLoggedIn, navigate, searchParams]);

  const handleGoogleLogin = async () => {
    setLoadingProvider('google');
    try {
      const returnTo = searchParams.get('returnTo') || '/';
      const redirectUrl = `${window.location.origin}${returnTo}`;
      const { error } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: { redirectTo: redirectUrl },
      });
      if (error) throw error;
    } catch (error: any) {
      toast({
        title: 'Google login failed',
        description: error.message || 'Could not sign in with Google',
        variant: 'destructive',
      });
      setLoadingProvider(null);
    }
  };

  const handleForgotPassword = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!email) return;

    setIsLoading(true);
    try {
      const { error } = await supabase.auth.resetPasswordForEmail(email, {
        redirectTo: `${window.location.origin}/reset-password`,
      });
      if (error) throw error;
      setResetEmailSent(true);
      toast({
        title: 'Reset email sent! ðŸ“§',
        description: 'Check your inbox for the password reset link.',
      });
    } catch (error: any) {
      toast({
        title: 'Failed to send reset email',
        description: error.message || 'Please try again later.',
        variant: 'destructive',
      });
    } finally {
      setIsLoading(false);
    }
  };

  const handleEmailAuth = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!email || !password) return;

    setIsLoading(true);
    try {
      if (mode === 'signup') {
        const { data, error } = await supabase.auth.signUp({
          email,
          password,
          options: {
            emailRedirectTo: `${window.location.origin}/`,
            data: { name: name || email.split('@')[0] },
          },
        });
        if (error) throw error;
        if (data.user) {
          login({
            id: data.user.id,
            email: data.user.email || email,
            name: name || email.split('@')[0],
            provider: 'email',
          });
          toast({
            title: 'Account created! ðŸŽ‰',
            description: 'You are now signed in.',
          });
          navigate(searchParams.get('returnTo') || '/');
        }
      } else {
        const { data, error } = await supabase.auth.signInWithPassword({
          email,
          password,
        });
        if (error) throw error;
        if (data.user) {
          login({
            id: data.user.id,
            email: data.user.email || email,
            name: data.user.user_metadata?.name || email.split('@')[0],
            provider: 'email',
          });
          toast({
            title: 'Welcome back!',
            description: `Signed in as ${email}`,
          });
          navigate(searchParams.get('returnTo') || '/');
        }
      }
    } catch (error: any) {
      let message = error.message || 'Authentication failed';
      if (error.message?.includes('already registered')) {
        message = 'This email is already registered. Try signing in instead.';
      } else if (error.message?.includes('Invalid login')) {
        message = 'Invalid email or password. Please try again.';
      }
      toast({
        title: mode === 'signup' ? 'Signup failed' : 'Login failed',
        description: message,
        variant: 'destructive',
      });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-background flex items-center justify-center p-4">
      {/* Background gradient */}
      <div className="fixed inset-0 bg-gradient-to-br from-primary/5 via-background to-secondary/5 pointer-events-none" />
      
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        className="relative w-full max-w-md"
      >
        {/* Back to home */}
        <Button
          variant="ghost"
          size="sm"
          onClick={() => navigate('/')}
          className="absolute -top-12 left-0 text-muted-foreground hover:text-foreground"
        >
          <Home className="w-4 h-4 mr-2" />
          Back to Home
        </Button>

        <div className="bg-card border border-border rounded-2xl shadow-2xl overflow-hidden">
          {/* Header */}
          <div className="relative px-8 pt-8 pb-6 text-center bg-gradient-to-b from-primary/10 to-transparent">
            <div className="w-14 h-14 mx-auto mb-4 rounded-xl bg-primary/20 flex items-center justify-center">
              {mode === 'forgot' ? (
                <KeyRound className="w-7 h-7 text-primary" />
              ) : (
                <Sparkles className="w-7 h-7 text-primary" />
              )}
            </div>
            <h1 className="text-2xl font-bold">
              {mode === 'forgot' 
                ? 'Reset your password' 
                : mode === 'signup'
                ? 'Create your account'
                : 'Welcome back'}
            </h1>
            <p className="text-muted-foreground mt-2 text-sm">
              {mode === 'forgot'
                ? "Enter your email and we'll send you a reset link"
                : mode === 'signup' 
                ? 'Sign up to save your pitch recordings' 
                : 'Sign in to access your recordings'}
            </p>
          </div>

          {/* Form */}
          <div className="px-8 pb-8 space-y-4">
            {mode === 'forgot' ? (
              <>
                {resetEmailSent ? (
                  <div className="text-center space-y-4 py-4">
                    <div className="w-16 h-16 mx-auto rounded-full bg-green-500/10 flex items-center justify-center">
                      <Mail className="w-8 h-8 text-green-500" />
                    </div>
                    <div>
                      <h3 className="font-semibold text-lg">Check your email</h3>
                      <p className="text-sm text-muted-foreground mt-1">
                        We've sent a password reset link to <strong>{email}</strong>
                      </p>
                    </div>
                    <Button variant="outline" onClick={() => setMode('login')}>
                      <ArrowLeft className="w-4 h-4 mr-2" />
                      Back to login
                    </Button>
                  </div>
                ) : (
                  <form onSubmit={handleForgotPassword} className="space-y-4">
                    <div className="space-y-2">
                      <Label htmlFor="reset-email">Email address</Label>
                      <Input
                        id="reset-email"
                        type="email"
                        placeholder="you@example.com"
                        value={email}
                        onChange={(e) => setEmail(e.target.value)}
                        disabled={isLoading}
                        autoFocus
                      />
                    </div>
                    <Button
                      type="submit"
                      className="w-full h-12"
                      disabled={isLoading || !email}
                    >
                      {isLoading && <Loader2 className="w-4 h-4 mr-2 animate-spin" />}
                      Send Reset Link
                    </Button>
                    <button
                      type="button"
                      onClick={() => setMode('login')}
                      className="w-full text-center text-sm text-muted-foreground hover:text-foreground"
                    >
                      <ArrowLeft className="w-3 h-3 inline mr-1" />
                      Back to login
                    </button>
                  </form>
                )}
              </>
            ) : (
              <>
                {/* Google OAuth */}
                <Button
                  variant="outline"
                  className="w-full h-12 gap-3 text-base font-medium"
                  onClick={handleGoogleLogin}
                  disabled={!!loadingProvider || isLoading}
                >
                  {loadingProvider === 'google' ? (
                    <Loader2 className="w-5 h-5 animate-spin" />
                  ) : (
                    <GoogleIcon />
                  )}
                  Continue with Google
                </Button>

                {/* Divider */}
                <div className="relative">
                  <div className="absolute inset-0 flex items-center">
                    <div className="w-full border-t border-border" />
                  </div>
                  <div className="relative flex justify-center text-xs uppercase">
                    <span className="bg-card px-2 text-muted-foreground">or</span>
                  </div>
                </div>

                {/* Email form */}
                <form onSubmit={handleEmailAuth} className="space-y-4">
                  {mode === 'signup' && (
                    <div className="space-y-2">
                      <Label htmlFor="name">Name</Label>
                      <Input
                        id="name"
                        type="text"
                        placeholder="Your name"
                        value={name}
                        onChange={(e) => setName(e.target.value)}
                        disabled={isLoading || !!loadingProvider}
                      />
                    </div>
                  )}
                  
                  <div className="space-y-2">
                    <Label htmlFor="email">Email</Label>
                    <Input
                      id="email"
                      type="email"
                      placeholder="you@example.com"
                      value={email}
                      onChange={(e) => setEmail(e.target.value)}
                      disabled={isLoading || !!loadingProvider}
                    />
                  </div>
                  
                  <div className="space-y-2">
                    <div className="flex items-center justify-between">
                      <Label htmlFor="password">Password</Label>
                      {mode === 'login' && (
                        <button
                          type="button"
                          onClick={() => setMode('forgot')}
                          className="text-xs text-primary hover:underline"
                        >
                          Forgot password?
                        </button>
                      )}
                    </div>
                    <Input
                      id="password"
                      type="password"
                      placeholder="â€¢â€¢â€¢â€¢â€¢â€¢â€¢â€¢"
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      disabled={isLoading || !!loadingProvider}
                    />
                  </div>

                  <Button
                    type="submit"
                    className="w-full h-12 text-base font-medium"
                    disabled={isLoading || !!loadingProvider || !email || !password}
                  >
                    {isLoading && <Loader2 className="w-4 h-4 mr-2 animate-spin" />}
                    {mode === 'signup' ? 'Create Account' : 'Sign In'}
                  </Button>
                </form>

                {/* Toggle mode */}
                <p className="text-center text-sm text-muted-foreground">
                  {mode === 'signup' ? (
                    <>
                      Already have an account?{' '}
                      <button
                        onClick={() => setMode('login')}
                        className="text-primary hover:underline font-medium"
                      >
                        Sign in
                      </button>
                    </>
                  ) : (
                    <>
                      Don't have an account?{' '}
                      <button
                        onClick={() => setMode('signup')}
                        className="text-primary hover:underline font-medium"
                      >
                        Sign up
                      </button>
                    </>
                  )}
                </p>
              </>
            )}
          </div>
        </div>
      </motion.div>
    </div>
  );
}
